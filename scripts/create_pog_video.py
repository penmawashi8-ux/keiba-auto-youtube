_MARKER_RE = re.compile(r'^【[^】]+】$')


def strip_chapter_markers(script: str) -> str:
    """TTS用: 【...】だけの行を除去し、連続空行を整理する。"""
    lines = [ln for ln in script.splitlines() if not _MARKER_RE.match(ln.strip())]
    return re.sub(r'\n{3,}', '\n\n', '\n'.join(lines)).strip()


def build_chapters_info(script: str) -> list[dict]:
    """各章マーカーの stripped スクリプト内の文字位置を計算して返す。
    pog_landscape_video.py が章の開始時刻を正確に算出するために使用。
    """
    stripped_pos = 0
    result = []
    for line in script.splitlines():
        s = line.strip()
        if _MARKER_RE.match(s):
            result.append({"title": s, "stripped_char_pos": stripped_pos})
        else:
            stripped_pos += len(s)
    return result


