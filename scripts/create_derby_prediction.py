def generate_derby_backgrounds() -> None:
    """日本ダービー専用の背景画像3枚を ffmpeg geq フィルターで生成する（Pillow不使用）。
    ダービーのイメージ（東京競馬場・威厳・芝の緑）でネイビー〜ゴールド系3パターン生成。
    assets/ai_N.jpg に保存し、generate_video.py が優先的に使用する。
    """
    Path(ASSETS_DIR).mkdir(exist_ok=True)

    backgrounds = [
        # 0: 漆黒→ロイヤルネイビー（ダービーの威厳）
        (
            "ai_0.jpg",
            "geq="
            "r='clip(4+20*pow(Y/H,2),4,24)':"
            "g='clip(6+40*pow(Y/H,1.8),6,46)':"
            "b='clip(10+180*pow(Y/H,1.4),10,190)'",
        ),
        # 1: 漆黒→エメラルドグリーン（東京競馬場の芝）
        (
            "ai_1.jpg",
            "geq="
            "r='clip(4+20*pow(Y/H,2),4,24)':"
            "g='clip(10+160*pow(Y/H,1.4),10,170)':"
            "b='clip(6+60*pow(Y/H,1.8),6,66)'",
        ),
        # 2: 漆黒→ゴールド（勝利の輝き）
        (
            "ai_2.jpg",
            "geq="
            "r='clip(8+180*pow(1-Y/H,1.4),8,188)':"
            "g='clip(6+140*pow(1-Y/H,1.6),6,146)':"
            "b='clip(4+10*pow(Y/H,2),4,14)'",
        ),
    ]

    for filename, vf_expr in backgrounds:
        out_path = f"{ASSETS_DIR}/{filename}"
        subprocess.run(
            [
                "ffmpeg", "-y",
                "-f", "lavfi", "-i", "color=black:s=1080x1920:r=1",
                "-vf", vf_expr,
                "-frames:v", "1",
                "-q:v", "3",
                out_path,
            ],
            check=True,
            capture_output=True,
        )
        print(f"背景画像を生成: {out_path}")


